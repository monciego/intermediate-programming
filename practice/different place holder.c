#include <Stdio.h>
int main ()
{
printf("Hello World!\n");
printf("%d \n", 32); ///integer
printf("%ld \n", 3245789); ////large integer
printf("%f \n", 32.2); ///float
printf("%lf \n",3261717.12737); ///large float
printf("%c \n",'a'); //char
printf("%s \n","Hello World!"); ///string
printf("%x \n", 11); ///hexa
return 0;
}

